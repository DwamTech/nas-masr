                    'أمن وحراسة',
                ],
                'خدمة عملاء' => [
                    'موظف كول سنتر',
                    'أخصائي خدمة عملاء',
                    'استقبال عملاء',
                ],
            ],
        ];

        foreach ($sections as $slug => $mainSubs) {
            $category = Category::where('slug', $slug)->first();

            if (!$category) {
                continue;
            }

            $mainSort = 1;

            foreach ($mainSubs as $mainName => $subList) {
                // إنشاء أو تحديث القسم الرئيسي
                $main = CategoryMainSection::updateOrCreate(
                    [
                        'category_id' => $category->id,
                        'name' => $mainName,
                    ],
                    [
                        'sort_order' => $mainSort++,
                        'is_active' => true,
                    ]
                );

                $subSort = 1;

                foreach ($subList as $subName) {
                    CategorySubSection::updateOrCreate(
                        [
                            'category_id' => $category->id,
                            'main_section_id' => $main->id,
                            'name' => $subName,
                        ],
                        [
                            'sort_order' => $subSort++,
                            'is_active' => true,
                        ]
                    );
                }
            }
        }
    }
